public class Tipos{
    public static void main(String[] args) {

        /*
         * Sintaxe
         * <tipo> <nome> = <valor inicial da variavel (opcional)>
         */


        int idade = 10; // Uma variavel do tipo inteiro para armazenar numeros reais (64 bytes)
        float salario = 1.200f; // Uma variavel do tipo float para armazenar numeros com pontos flutuantes (32 bytes)
        double valor = 459.99; // Uma variavel do tipo double para armazenar numeros com pontos flutuantes (64 bytes)
        boolean chuveu = true; // Uma variavel do tipo boolean para declarar se é verdadeiro ou falso
        String texto = "Hello World"; // Uma variavel do tipo String para declarar uma cadeia de caracter (texto)
        char sexo = 'F'; // Uma variavel do tipo char para declarar apenas 1(Um) caracter.

    }
}


